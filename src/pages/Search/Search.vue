<script>
import SearchSelect from "./SearchSelect/SearchSelect";
import { mapGetters, mapState } from "vuex";
export default {
  name: "Search",
  data() {
    return {
      searchParams: {
        // 一级分类的id
        category1Id: "",
        // 二级分类的id
        category2Id: "",
        // 三级分类的id
        category3Id: "",
        //  分类名字
        categoryName: "",
        // 关键字
        keyword: "",
        // 排序
        order: "1:asc",
        // 分页器
        pageNo: 1,
        // 每一页展示数据个数
        pageSize: 3,
        //平台售卖属性参数
        props: [],
        // 品牌
        trademark: "",
      },
    };
  },
  components: {
    SearchSelect,
  },
  //当组件挂载完毕之前执行一次
  beforeMount() {
    // 复杂写法
    /* this.searchParams.category1Id = this.$route.query.category1Id;
    this.searchParams.category2Id = this.$route.query.category2Id;
    this.searchParams.category3Id = this.$route.query.category3Id;
    this.searchParams.categoryName = this.$route.query.categoryName;
    this.searchParams.keyWord = this.$route.query.keyWord; */
    // 再发请求前，将接口需要传递的参数，进行整理，服务器会返回查询的数据
    Object.assign(this.searchParams, this.$route.query, this.$route.params);
  },
  // 组件挂载完执行一次
  mounted() {
    this.getData();
  },
  computed: {
    // 获取仓库中计算属性
    ...mapGetters(["goodsList", "attrsList", "trademarkList", "totalList"]),
    isOne() {
      return this.searchParams.order.indexOf("1") != -1;
    },
    isTwo() {
      return this.searchParams.order.indexOf("2") != -1;
    },
    // 是否升序
    isAsc() {
      return this.searchParams.order.indexOf("asc") != -1;
    },
    //是否降序
    isDesc() {
      return this.searchParams.order.indexOf("desc") != -1;
    },
  },
  methods: {
    // 向服务器发送请求，请求search模块数据
    // 封装为函数，在需要时调用
    getData() {
      this.$store.dispatch("getSearchList", this.searchParams);
    },
    removeCategoryName() {
      // 把带给服务器的数据置空了，还需向服务器发送请求，得到默认数据
      // 由于接口post请求，没有要求那个参数是必须携带的，将参数置为undefined，可减小服务器压力
      this.searchParams.categoryName = undefined;
      this.searchParams.category1Id = undefined;
      this.searchParams.category2Id = undefined;
      this.searchParams.category3Id = undefined;
      this.getData();
      // 写到这里发现地址栏路径没变，需要跳转路由来改变路径，操作=向自己路由跳转使路径改变
      // 严谨：本意是删除query参数，如果路径中存在params参数 应该保留
      if (this.$route.params) {
        this.$router.push({ name: "search", params: this.$route.params });
      }
    },
    // 删除关键字
    removeKeyword() {
      this.searchParams.keyword = undefined;
      this.getData();
      // 通知兄弟组件header清空input框
      this.$bus.$emit("clear");
      if (this.$route.query) {
        this.$router.push({ name: "search", query: this.$route.query });
      }
    },
    // 删除商标面包屑
    removeTrademark() {
      this.searchParams.trademark = undefined;
      this.getData();
    },
    // 自定义事件
    trademarkInfo(trademark) {
      console.log("我是父组件", trademark);
      this.searchParams.trademark = `${trademark.tmId}:${trademark.tmName}`;
      this.getData();
    },
    // 自定义事件 平台数据
    getAttrInfo(attr, item) {
      // props 参数 格式 ["属性ID:属性值:属性名"]
      console.log(attr, item);
      // 整理参数格式
      let props = `${attr.attrId}:${item}:${attr.attrName}`;
      // 重点：数组去重  if语句里面只有一行代码 可省略大花括号
      if (this.searchParams.props.indexOf(props) == -1)
        this.searchParams.props.push(props);
      // 再次发送请求
      this.getData();
    },
    // 删除attr 平台数据 比如手机的128G
    removeAttr(index) {
      // 整理参数
      this.searchParams.props.splice(index, 1);
      // 再次发送请求
      this.getData();
    },
    // 改变排序（按综合/按价格）
    changeSort(flag) {
      // flag 判断用户点击的是哪个（综合/价格）
      // console.log(flag);
      // 获取最开始 点击哪个或升序还是降序，最开始状态
      let originFlag = this.searchParams.order.split(":")[0];
      let originSort = this.searchParams.order.split(":")[1];
      // 创建一个空对象
      let newOrder = "";
      if (flag == originFlag) {
        // 如果点击的仍是原来选中的sort，就将order参数设originFlag:
        newOrder = `${originFlag}:${originSort == "desc" ? "asc" : "desc"}`;
      } else {
        newOrder = `${flag}:desc`;
      }
      console.log(originFlag, originSort, newOrder);
      // 将新的order赋给searchParams参数
      this.searchParams.order = newOrder;
      // 再次发送请求
      this.getData();
    },
    // 自定义事件的回调
    getPageNo(pageNo) {
      // console.log(pageNo);
      // 整理带给服务器的参数
      this.searchParams.pageNo = pageNo;
      // 再次发送请求
      this.getData();
    },
  },
  // 监听 $route的变化(即路由信息的变化)，重新向服务器请求数据，需要重新整合请求传递的参数，
  watch: {
    $route() {
      // 01=首先重新整合数据
      Object.assign(this.searchParams, this.$route.query, this.$route.params);
      this.getData();
      // 请求后将category1Id category2Id category3Id中清空，才能再发送请求
      this.searchParams.category1Id = undefined;
      this.searchParams.category2Id = undefined;
      this.searchParams.category3Id = undefined;
    },
  },
};
</script>

